# improve how the 'current section' is detected
s/parseInt(\([^;]*\)\.css("width"),10)/\1[0].getBoundingClientRect().width/g;

# make buttons actual buttons, hide the icon from screen readers
s/<a>/<button>/; s/class:opts.icon/class:opts.icon,'aria-hidden':true/; s/,href:"#"//;

# add aria-expanded to dropdown menus
s/(toggleDropdown\(e\){)/\1e.currentTarget.setAttribute('aria-expanded',e.currentTarget.getAttribute('aria-expanded')!=='true');/;
s/(\("\.dropdown-menu"\)\.removeClass\("open"\))/\1;\$('.toggle-dropdown').attr('aria-expanded','false')/;
s/(addClass\("toggle-dropdown"\))/\1.attr('aria-expanded','false')/;
s/(,closeDropdown\))/\1;gitbook.keyboard.bind('escape',closeDropdown)/;

# we use <span class="bml-separator"> instead of <h1>
s/h1/span.bml-separator/g;

# add aria-label to buttons
s/(btn\.text)/\1,...'label' in btn&&{title:btn.label,'aria-label':btn.label}/;
